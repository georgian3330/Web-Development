var a = 2;
var b = 10;

// Add two numbers
var c = a + b;

// Subtract two numbers
var d = b - a;

// Multiply two numbers
var e = a * b;

// Divide two numbers
var f = b / a;

// Modulo operator
var g = b % a;

console.log("Addition: " + c + "\nSubtraction: " + d + "\nMultiplication: " + e + "\nDivision: " + f + "\nModulo: " + g);

